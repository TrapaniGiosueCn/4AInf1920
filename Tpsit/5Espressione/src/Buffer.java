public class Buffer {
    public double th1, th2, y, a, b;

    public Buffer() {
        this.th1 = 0;
        this.th2 = 0;
        this.y = 0;
        this.a = 0;
        this.b = 0;
    }

    public Buffer(double aa, double bb) {
        this.th1 = 0;
        this.th2 = 0;
        this.y = 0;
        this.a = aa;
        this.b = bb;
    }
}
